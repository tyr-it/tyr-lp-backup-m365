let currentLang = localStorage.getItem('tyr-lang') ||
  (navigator.language || navigator.userLanguage || 'pt').substring(0, 2);
if (!translations[currentLang]) currentLang = 'pt';

function i18n(key) {
  return (translations[currentLang] && translations[currentLang][key]) ||
         (translations['pt'] && translations['pt'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = i18n(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = i18n(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.textContent.toLowerCase() === currentLang);
  });
  document.documentElement.lang = currentLang === 'pt' ? 'pt-BR' : currentLang;
}

function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('tyr-lang', lang);
  applyTranslations();
}

document.addEventListener('DOMContentLoaded', applyTranslations);
